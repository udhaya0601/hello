
package com.programs;

public class SwappingwithThirdParty {
public static void main(String[] args) {
	int a=100;
	int b=500;
	System.out.println("before swaping");
	System.out.println("a = "+a);
	System.out.println("b = "+b);
	int c;
	 c=a;
	 a=b;
	 b=c;
	 System.out.println("after swaping");
		System.out.println("a = "+a);
		System.out.println("b = "+b);
	
	
	
	
}
}
