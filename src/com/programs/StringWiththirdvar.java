package com.programs;

public class StringWiththirdvar {
	public static void main(String[] args) {
		String a="greens";
		String b="color";
		System.out.println("before swapping");
		System.out.println(a);
		System.out.println(b);		
		String c;
		c=a;
		a=b;
		b=c;
		
		
		System.out.println("after swapping");
		System.out.println(a);
		System.out.println(b);		
		
		
		
		
		
		
		
	}

}
